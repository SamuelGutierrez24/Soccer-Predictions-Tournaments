-- Companies
CREATE TABLE companies (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name VARCHAR(100) NOT NULL UNIQUE,
    nit VARCHAR(100) NOT NULL UNIQUE,
    address VARCHAR(100),
    contact VARCHAR(100),
    logo VARCHAR(100),
    deleted_at TIMESTAMP NULL
);

-- Platform Config
CREATE TABLE platform_config (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    tournament_champion INT,
    team_with_most_goals INT,
    exact_score INT,
    match_winner INT
);

-- Permissions
CREATE TABLE permissions (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name VARCHAR(100) NOT NULL,
    deleted_at TIMESTAMP NULL
);

-- Roles
CREATE TABLE roles (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name VARCHAR(100) NOT NULL UNIQUE,
    deleted_at TIMESTAMP NULL
);

-- Table: teams
CREATE TABLE teams (
    id BIGINT PRIMARY KEY,
    name VARCHAR(255) NOT NULL,
    logo_url TEXT,
    deleted_at TIMESTAMP NULL
);

-- Table: tournaments
CREATE TABLE tournaments (
    id BIGINT PRIMARY KEY,
    name VARCHAR(255) NOT NULL,
    description TEXT NOT NULL,
    winner_team_id BIGINT,
    fewest_goals_conceded_team_id BIGINT,
    top_scoring_team_id BIGINT,
    start_date DATE,
    final_date DATE,
    tournament_logo TEXT,
    deleted_at TIMESTAMP NULL,
    CONSTRAINT fk_tournament_winner_team FOREIGN KEY (winner_team_id) REFERENCES teams(id),
    CONSTRAINT fk_tournament_fewest_goals_team FOREIGN KEY (fewest_goals_conceded_team_id) REFERENCES teams(id),
    CONSTRAINT fk_tournament_top_scoring_team FOREIGN KEY (top_scoring_team_id) REFERENCES teams(id)
);

-- Table: tournament_statistics
CREATE TABLE tournament_statistics (
    tournament_id BIGINT PRIMARY KEY,
    winner_team_id BIGINT,
    fewest_goals_conceded_team_id BIGINT,
    top_scoring_team_id BIGINT,
    top_scorer_id BIGINT,
    top_scorer_name VARCHAR(255),
    top_scorer_team_id BIGINT,
    top_scorer_urlimg TEXT,
    created_at TIMESTAMP,
    updated_at TIMESTAMP,
    CONSTRAINT fk_stats_winner_team FOREIGN KEY (winner_team_id) REFERENCES teams(id),
    CONSTRAINT fk_stats_fewest_goals_team FOREIGN KEY (fewest_goals_conceded_team_id) REFERENCES teams(id),
    CONSTRAINT fk_stats_top_scoring_team FOREIGN KEY (top_scoring_team_id) REFERENCES teams(id),
    CONSTRAINT fk_stats_top_scorer_team FOREIGN KEY (top_scorer_team_id) REFERENCES teams(id),
    CONSTRAINT fk_stats_tournament FOREIGN KEY (tournament_id) REFERENCES tournaments(id) ON DELETE CASCADE
);

-- Table: stages
CREATE TABLE stages (
    id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    stage_name VARCHAR(100) NOT NULL,
    tournament_id BIGINT NOT NULL,
    deleted_at TIMESTAMP NULL,
    CONSTRAINT fk_stage_tournament FOREIGN KEY (tournament_id) REFERENCES tournaments(id) ON DELETE CASCADE
);

-- Type Notification
CREATE TABLE type_notification (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name VARCHAR(50) NOT NULL UNIQUE,
    description VARCHAR(200)
);

-- Users
CREATE TABLE users (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    cedula VARCHAR(20) NOT NULL,
    company_id BIGINT NOT NULL,
    name VARCHAR(255) NOT NULL,
    last_name VARCHAR(255),
    password TEXT NOT NULL,
    mail VARCHAR(255) NOT NULL,
    nickname VARCHAR(100) UNIQUE NOT NULL,
    phone_number VARCHAR(20),
    photo TEXT,
    role_id BIGINT NOT NULL,
    notifications_email_enabled BOOLEAN NOT NULL DEFAULT FALSE,
    notifications_sms_enabled BOOLEAN NOT NULL DEFAULT FALSE,
    notifications_whatsapp_enabled BOOLEAN NOT NULL DEFAULT FALSE,
    extra_info JSON,
    deleted_at TIMESTAMP NULL
);

-- group_stage (Note: Table name is "group_stage" with quotes and capital 'G' and 'S' as per entity)
CREATE TABLE "group_stage" (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    group_name VARCHAR(100) NOT NULL,
    tournament_id BIGINT NOT NULL,
    first_winner_team_id BIGINT,
    second_winner_team_id BIGINT,
    deleted_at TIMESTAMP NULL
);

-- Matches
CREATE TABLE matches (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    date TIMESTAMP NOT NULL,
    status VARCHAR(50),
    home_team_id BIGINT,
    away_team_id BIGINT,
    home_score INT,
    away_score INT,
    extra_time BOOLEAN,
    extra_home_score INT,
    extra_away_score INT,
    penalty BOOLEAN,
    penalty_home INT,
    penalty_away INT,
    winner_team_id BIGINT,
    tournament_id BIGINT NOT NULL,
    stage_id BIGINT NOT NULL, -- Note: stages.id is INT, matches.stage_id is BIGINT. FK will be BIGINT -> INT.
    deleted_at TIMESTAMP NULL
);

-- Table: match_statistics
CREATE TABLE match_statistics (
    id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    team_name VARCHAR(255),
    logo TEXT,
    match_id INT UNIQUE, -- Note: matches.id is BIGINT, match_statistics.match_id is INT. FK will be INT -> BIGINT.
    shots_on_goal INT,
    shots_off_goal INT,
    total_shots INT,
    blocked_shots INT,
    shots_inside_box INT,
    shots_outside_box INT,
    fouls INT,
    corner_kicks INT,
    offsides INT,
    ball_possession VARCHAR(10),
    yellow_cards INT,
    red_cards INT,
    goalkeeper_saves INT,
    total_passes INT,
    passes_accurate INT,
    passes_percentage VARCHAR(10),
    CONSTRAINT fk_match_stats_match FOREIGN KEY (match_id) REFERENCES matches(id) ON DELETE CASCADE
);

-- Table: pollas
CREATE TABLE pollas (
    id BIGINT GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    start_date TIMESTAMP NOT NULL,
    end_date TIMESTAMP NOT NULL,
    private BOOLEAN NOT NULL,
    image_url TEXT,
    is_active BOOLEAN,
    company_id BIGINT,
    platform_config_id BIGINT,
    tournament_id BIGINT NOT NULL,
    deleted_at TIMESTAMP NULL,
    color VARCHAR(255),
    CONSTRAINT fk_polla_company FOREIGN KEY (company_id) REFERENCES companies(id),
    CONSTRAINT fk_polla_platform_config FOREIGN KEY (platform_config_id) REFERENCES platform_config(id),
    CONSTRAINT fk_polla_tournament FOREIGN KEY (tournament_id) REFERENCES tournaments(id)
);

-- SubPollas
CREATE TABLE subpollas (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    "private" BOOLEAN NOT NULL, -- Quoted because 'private' is a keyword
    polla_id BIGINT NOT NULL,
    creator_user_id BIGINT NOT NULL,
    deleted_at TIMESTAMP NULL
);

-- Notification
CREATE TABLE notification (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    user_id BIGINT NOT NULL,
    content VARCHAR(500) NOT NULL,
    timestamp TIMESTAMP NOT NULL,
    type_id BIGINT NOT NULL,
    read BOOLEAN NOT NULL DEFAULT FALSE
);

-- Rewards
CREATE TABLE rewards (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name VARCHAR(255) NOT NULL,
    description TEXT,
    image TEXT,
    position INT,
    polla_id BIGINT NOT NULL
);

-- Tournament Bets
CREATE TABLE tournament_bets (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    earned_points INT NOT NULL,
    user_id BIGINT,
    polla_id BIGINT,
    tournament_id BIGINT,
    winner_team_id BIGINT,
    top_scoring_team_id BIGINT,
    deleted_at TIMESTAMP NULL
);

-- Match Bets
CREATE TABLE match_bets (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    home_score INT NOT NULL,
    away_score INT NOT NULL,
    earned_points INT,
    user_id BIGINT NOT NULL,
    match_id BIGINT NOT NULL,
    polla_id BIGINT NOT NULL,
    deleted_at TIMESTAMP NULL
);

-- SubPolla Join Requests
CREATE TABLE subpolla_join_requests (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    user_id BIGINT NOT NULL,
    subpolla_id BIGINT NOT NULL,
    status VARCHAR(255) NOT NULL,
    deleted_at TIMESTAMP NULL
);

-- Permission Role (Join Table for Role <-> Permission)
CREATE TABLE permission_role (
    role_id BIGINT NOT NULL,
    permission_id BIGINT NOT NULL,
    PRIMARY KEY (role_id, permission_id)
);

-- Group Stage Team (Join Table for group_stage <-> Team with extra columns)
CREATE TABLE group_stage_team (
    group_id BIGINT NOT NULL,
    team_id BIGINT NOT NULL,
    points INT,
    rank INT,
    PRIMARY KEY (group_id, team_id)
);

-- Group Stage Match (Join Table for group_stage <-> Match)
CREATE TABLE group_stage_match (
    group_id BIGINT NOT NULL,
    match_id BIGINT NOT NULL,
    PRIMARY KEY (group_id, match_id)
);

-- User Scores Polla (Composite Key Table)
CREATE TABLE user_scores_polla (
    user_id BIGINT NOT NULL,
    polla_id BIGINT NOT NULL,
    state VARCHAR(255) NOT NULL,
    scores INT NOT NULL,
    PRIMARY KEY (user_id, polla_id)
);

-- User SubPolla (Composite Key Table for User <-> SubPolla)
CREATE TABLE user_subpolla (
    user_id BIGINT NOT NULL,
    subpolla_id BIGINT NOT NULL,
    PRIMARY KEY (user_id, subpolla_id)
);

